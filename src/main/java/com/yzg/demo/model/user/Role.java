package com.yzg.demo.model.user;

//角色信息，比如超用户admin/普通用户normal/运维人员op,等
public class Role {
    int id;
    String name;
    String comment;

    public Role(int id, String name, String comment) {
        this.id = id;
        this.name = name;
        this.comment = comment;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
